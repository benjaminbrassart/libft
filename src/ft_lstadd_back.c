#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*lst;

	if (alst)
	{
		lst = ft_lstlast(*alst);
		if (lst)
			lst->next = new;
		else
			*alst = new;
	}
}

#include "ft.h"

int	ft_atoi(char const *s)
{
	int	i;
	int	sign;

	i = 0;
	sign = 1;
	while (*s == ' ' || (*s >= '\t' && *s <= '\r'))
		++s;
	if (*s == '-' || *s == '+')
		if (*s++ == '-')
			sign = -1;
	while (ft_isdigit(*s))
		i = i * 10 + *s++ - '0';
	return (i * sign);
}
